# add two numbers
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
print("Sum:", a + b)

# subtract two numbers
print("Difference:", a - b)

# swap two numbers using temp
temp = a
a = b
b = temp
print("After swapping:")
print("a =", a)
print("b =", b)

